function sumar() {
    let numero1 = document.getElementById('numero1').value;
    let numero2 = document.getElementById('numero2').value;

    console.log(parseInt(numero1) + parseInt(numero2));
}

function restar() {
    let numero1 = document.getElementById('numero1').value;
    let numero2 = document.getElementById('numero2').value;

    console.log(parseInt(numero1) - parseInt(numero2));
}

function imprimir() {
    let numero1 = document.getElementById('numero1').value;
    let numero2 = document.getElementById('numero2').value;

    console.log(numero1);
    console.log(numero2);
}


